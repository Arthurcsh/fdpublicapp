package com.wondersgroup.fdpublicapp.personal.mode;

/**
 *  个人中心数字信息
 * @author chengshaohua
 *
 */
public class FDUserInfo {

	private int countOfComments;          // 我的点评数
	private int countOfEvent;
	private int countOfDiscount;          // 收藏的优惠
	private int countOfRecommends;        // 我的推荐数
	private int countOfCollect;           // 我的收藏数
	private int countOfMsg;               // 未读消息数
	
	public int getCountOfComments() {
		return countOfComments;
	}
	public void setCountOfComments(int countOfComments) {
		this.countOfComments = countOfComments;
	}
	public int getCountOfEvent() {
		return countOfEvent;
	}
	public void setCountOfEvent(int countOfEvent) {
		this.countOfEvent = countOfEvent;
	}
	public int getCountOfDiscount() {
		return countOfDiscount;
	}
	public void setCountOfDiscount(int countOfDiscount) {
		this.countOfDiscount = countOfDiscount;
	}
	public int getCountOfRecommends() {
		return countOfRecommends;
	}
	public void setCountOfRecommends(int countOfRecommends) {
		this.countOfRecommends = countOfRecommends;
	}
	public int getCountOfCollect() {
		return countOfCollect;
	}
	public void setCountOfCollect(int countOfCollect) {
		this.countOfCollect = countOfCollect;
	}
	public int getCountOfMsg() {
		return countOfMsg;
	}
	public void setCountOfMsg(int countOfMsg) {
		this.countOfMsg = countOfMsg;
	}
	
}
