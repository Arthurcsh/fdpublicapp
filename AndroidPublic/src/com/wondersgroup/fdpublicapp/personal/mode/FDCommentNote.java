package com.wondersgroup.fdpublicapp.personal.mode;

import java.util.List;

import com.wondersgroup.fdpublicapp.home.nutritionMeal.mode.FDNoteComment;

/** 我评论的帖子
 * @author chengshaohua
 *
 */
public class FDCommentNote {
	private int postId;
	private String postTitle;
	private String postCreateTime;
	private List<FDNoteComment> commentList;
	
	public int getPostId() {
		return postId;
	}
	public void setPostId(int postId) {
		this.postId = postId;
	}
	public String getPostTitle() {
		return postTitle;
	}
	public void setPostTitle(String postTitle) {
		this.postTitle = postTitle;
	}
	public String getPostCreateTime() {
		return postCreateTime;
	}
	public void setPostCreateTime(String postCreateTime) {
		this.postCreateTime = postCreateTime;
	}
	public List<FDNoteComment> getCommentList() {
		return commentList;
	}
	public void setCommentList(List<FDNoteComment> commentList) {
		this.commentList = commentList;
	}
	
}
