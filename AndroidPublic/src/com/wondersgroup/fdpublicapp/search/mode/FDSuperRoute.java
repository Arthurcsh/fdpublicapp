package com.wondersgroup.fdpublicapp.search.mode;

import java.util.List;
import com.amap.api.services.core.LatLonPoint;

public class FDSuperRoute {

	protected int routeID;
	protected LatLonPoint startPoint;
	protected LatLonPoint targetPoint;
	protected float distance;
	
	protected List<FDSuperLine> lines;

	public int getRouteID() {
		return routeID;
	}

	public void setRouteID(int routeID) {
		this.routeID = routeID;
	}

	public LatLonPoint getStartPoint() {
		return startPoint;
	}

	public void setStartPoint(LatLonPoint startPoint) {
		this.startPoint = startPoint;
	}

	public LatLonPoint getTargetPoint() {
		return targetPoint;
	}

	public void setTargetPoint(LatLonPoint targetPoint) {
		this.targetPoint = targetPoint;
	}

	public float getDistance() {
		return distance;
	}

	public void setDistance(float distance) {
		this.distance = distance;
	}

	public List<FDSuperLine> getLines() {
		return lines;
	}

	public void setLines(List<FDSuperLine> lines) {
		this.lines = lines;
	}
	
	
}
