	public String getCuisineValue() {
		if(restCuisineTypeList==null) return "";
		
		StringBuffer cuisineValueBuffer = new StringBuffer();
		for (int k = 0; k < restCuisineTypeList.size(); k++) {
			FDCuisine cuisine = restCuisineTypeList.get(k);
			if(cuisine==null) continue;
			cuisineValueBuffer.append(cuisine.getCuisineValue());
			if (k != (restCuisineTypeList.size()-1)) {
				cuisineValueBuffer.append(",");
			}
		}
		return cuisineValueBuffer.toString();
	}
