package com.wondersgroup.fdpublicapp.home.main.mode;

import java.io.Serializable;

public class FDLocation implements Serializable {

	/**
	 *   地图定位信息
	 */
	private static final long serialVersionUID = 1L;

	private double latitude;
	private double longitude;         // 维度
	private double accuracy;          // 精度
	private String providerType;      // 定位方式
	private String locationTime;      // 定位时间
	private String cityCode;          // 定位城市
	private String addressCode;       // 城市编号
	private String addressDesc;       // 城市地址
	
	
	public double getLatitude() {
		return latitude;
	}
	public void setLatitude(double latitude) {
		this.latitude = latitude;
	}
	public double getLongitude() {
		return longitude;
	}
	public void setLongitude(double longitude) {
		this.longitude = longitude;
	}
	public double getAccuracy() {
		return accuracy;
	}
	public void setAccuracy(double accuracy) {
		this.accuracy = accuracy;
	}
	public String getProviderType() {
		return providerType;
	}
	public void setProviderType(String providerType) {
		this.providerType = providerType;
	}
	public String getLocationTime() {
		return locationTime;
	}
	public void setLocationTime(String locationTime) {
		this.locationTime = locationTime;
	}
	public String getCityCode() {
		return cityCode;
	}
	public void setCityCode(String cityCode) {
		this.cityCode = cityCode;
	}
	public String getAddressCode() {
		return addressCode;
	}
	public void setAddressCode(String addressCode) {
		this.addressCode = addressCode;
	}
	public String getAddressDesc() {
		return addressDesc;
	}
	public void setAddressDesc(String addressDesc) {
		this.addressDesc = addressDesc;
	}
	
	
}
