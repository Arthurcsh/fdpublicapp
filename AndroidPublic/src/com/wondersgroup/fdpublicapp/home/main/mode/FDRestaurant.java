	public static final Parcelable.Creator<FDRestaurant> CREATOR = new Creator<FDRestaurant>() {  
        public FDRestaurant createFromParcel(Parcel source) {  
        	FDRestaurant mRestaurant = new FDRestaurant();  
        	mRestaurant.id = source.readInt(); 
        	mRestaurant.type = source.readInt();
        	mRestaurant.name = source.readString();
        	mRestaurant.shopSign = source.readString();
        	mRestaurant.restaurantNameAbbrev = source.readString();
        	mRestaurant.foodSaftyRating = source.readInt();
        	mRestaurant.gisLatitude = source.readDouble();
        	mRestaurant.gisLongitude = source.readDouble();
        	mRestaurant.seats = source.readInt();
        	mRestaurant.statusValue = source.readString();
        	mRestaurant.registeredAddress = source.readString();
        	mRestaurant.bizAddress = source.readString();
        	
            return mRestaurant;  
        }  
        public FDRestaurant[] newArray(int size) {  
            return new FDRestaurant[size];  
        }  
    };  
