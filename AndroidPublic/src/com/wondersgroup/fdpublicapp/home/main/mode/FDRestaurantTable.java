package com.wondersgroup.fdpublicapp.home.main.mode;

import java.util.ArrayList;


public class FDRestaurantTable {

	private int currentPage = 1;
	private int pageCount;
	private ArrayList<FDRestaurant> restaurants;
	
	public int getCurrentPage() {
		return currentPage;
	}
	public void setCurrentPage(int currentPage) {
		this.currentPage = currentPage;
	}
	public int getPageCount() {
		return pageCount;
	}
	public void setPageCount(int pageCount) {
		this.pageCount = pageCount;
	}
	public ArrayList<FDRestaurant> getRestaurants() {
		return restaurants;
	}
	public void setRestaurants(ArrayList<FDRestaurant> restaurants) {
		this.restaurants = restaurants;
	}
	
}
